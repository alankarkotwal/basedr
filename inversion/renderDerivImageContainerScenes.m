function ims = renderDerivImageContainerScenes(sigmaT, sigmaS, func,...
												sigmaTSingle, sigmaSSingle, funcSingle,...
												theta, sceneSet, rendererSet)

numScenes = length(sceneSet);
assert(length(rendererSet) == numScenes);
ims = cell(numScenes, 1);
parfor iterScene = 1:numScenes,
	ims{iterScene} = renderDerivImageContainer(sigmaT, sigmaS, func,...
												sigmaTSingle, sigmaSSingle, funcSingle, theta,...
												sceneSet(iterScene), rendererSet(iterScene));
end;
